course = 'Python for Beginners'

# len() returns the length of object
print(len(course))

# uppercase
print(course.upper())

# lowercase
print(course.lower())

# find() method returns the index of 1st occurrence
print(course.find('P'))
print(course.find('p'))

# returns the index where the sequence starts
print(course.find('Beginners'))

# replace() method
print(course.replace('Beginners', 'Absolute Beginners'))
